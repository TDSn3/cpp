/*   OPÉRATEUR INTERNE   **************************************************** */

Dog	&Dog::operator=(const Dog &right)
{
	std::cout << "\033[33;02;03m" << "Dog : Copy assignment operator called" << "\033[00m" << std::endl;
	this->_type = right.getType();
	return (*this);
}

